import sklearn
#importing dataset
from sklearn.datasets import load_breast_cancer
#intialize the data set
data = load_breast_cancer()

# organize the data 
# column names
label_names = data['target_names']
labels = data['target']              
feature_names = data['feature_names']
feature = data['data']

print(label_names)
#  two labelled data

print(feature_names)

print(labels)
#malignant is 0 benign is 1

print(feature)

print(label_names)

print(labels)

print(data) # show the above outputs in a single  block

# data is splitting into two sets now
# importing the functions

from sklearn.model_selection import train_test_split

# splitting the data
# test data = 33% Training model data use = 67%
train , test , train_labels , test_labels = train_test_split(feature,
                                                             labels,
                                                             test_size = 0.33
                                                             , random_state = 42)

#Building The Model
from sklearn.naive_bayes import GaussianNB
#intialize the classifier
gnb = GaussianNB()
# Training the Classifier
model = gnb.fit(train,train_labels)

# Making Predictions

Predictions = gnb.predict(test)
print(Predictions)   # 1 is mag 0 is beniyn of 33%

# Evaluaate The  Trained Built Model

from sklearn.metrics import accuracy_score

print(accuracy_score(test_labels,Predictions))

# accuracy is 94.15%

